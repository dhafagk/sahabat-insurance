async function seed() {
  const payload = await getPayload({ config });

  const existing = await payload.find({
    collection: "tabel",
    where: { slug: { equals: "garage-list" } },
    limit: 1,
  });

  if (existing.totalDocs > 0) {
    console.log("Record already exists, updating...");
    await payload.update({
      collection: "tabel",
      id: existing.docs[0].id,
      data: {
        title: "Daftar Bengkel",
        status: "published",
        tables,
      },
    });
  } else {
    await payload.create({
      collection: "tabel",
      data: {
        title: "Daftar Bengkel",
        slug: "garage-list",
        status: "published",
        tables,
      },
    });
  }

  console.log("Done.");
  process.exit(0);
}

seed().catch((err) => {
  console.error(err);
  process.exit(1);
});
